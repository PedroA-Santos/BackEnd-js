//Faça um programa que verifique se um número é negativo, positivo ou zero.



function aaa (numero){

    if (numero <0 ){
        console.log( " é negativo")
    }else if (numero == 0){
        console.log( " é igual a 0")
    }else{
        console.log(" é positivo")
    }
}


console.log(aaa(5))
console.log(aaa(0))
console.log(aaa(-10))
